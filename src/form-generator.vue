<template>
<v-form ref="form" v-model="validForm" lazy-validation>
    <v-toolbar-title v-if="schema.title">
        Documento:{{ schema.title }}
    </v-toolbar-title>

        <!--<v-subheader v-if="schema.title">-->
            <!--<p>-->
            <!--<h3 class="display-1 titleDocumentLabel">-->
            <!--<span>Documento: </span><strong class="titleDocument">-->
            <!--{{ schema.title }}</strong>-->
        <!--</h3></p></v-subheader>-->
        <div v-for="(schemaItem, schemaItemIndex) in schema">
            <div v-if="schemaItemIndex == 'forms'">
                <v-stepper v-model="stepper">
                    <v-stepper-header>
                        <template v-for="(steps, index) in schemaItem">
                            <v-stepper-step
                                        :key="`${index+1}-step`"
                                        :step="index+1"
                                        :complete="stepper > index"
                                      >
                             {{ steps.label }}
                            </v-stepper-step>
                            <v-divider v-if="index+1 !== schemaItem.length" :key="`${index+1}-div`"></v-divider>
                        </template>
                    </v-stepper-header>
                    <v-stepper-items>
                        <v-stepper-content
                          v-for="(steps, index) in schemaItem"
                          :step="index+1"
                          :key="`${index+1}-content`"
                          row wrap
                        >
                            <v-layout
                                    row align-right justify-right v-if="steps.helpIcon=='true'">
                                <v-btn
                                    absolute
                                    dark
                                    fab
                                    right
                                    color="accent"
                                    @click="showHelp"
                                >
                                <v-icon x-large light>not_listed_location</v-icon>
                            </v-btn>
                            </v-layout>
                            <v-layout row wrap>
                                <v-flex xs12 lg12 xl12 >
                                    <v-card   ref="`${index+1}-vcontent`">

                                        <v-container  fluid grid-list-lg fill-height
                                        >

                                            <v-layout row align-center justify-center>
                                                <v-flex xs12 lg5 xl5>

                                                    <v-carousel  :cycle="false" hide-delimiters light>
                                                        <v-carousel-item v-for="(item,index) in imageArr"
                                                            cycle
                                                            :key="`${index}-carousel`">
                                                            <object
                                                              :data="item.images.normal_size[0].url"
                                                              v-if="typeMimeType(item.images.normal_size[0].url)==='OBJECT'"
                                                              style="min-height:20em; width:100%; height:100%"
                                                              container
                                                              >
                                                            </object>
                                                              <div height="100%"
                                                                    width="100%"
                                                                    style="min-height:20em" v-if="typeMimeType(item.images.normal_size[0].url)==='IMAGE'">
                                                                   <ProductZoomer
                                                                     :nameSpaceOptions="`step_${steps.id}_${index}`"
                                                                     :base-images="item.images"
                                                                     :base-zoomer-options="item.containerSquareOptions"/>
                                                              </div>
                                                        </v-carousel-item>
                                                    </v-carousel>
                                                </v-flex>
                                                <v-flex xs10 lg7 xl7  class="elevation-{28}">
                                                <div v-for="field in steps.fields" >
                                                     <div v-if="field.groups">
                                                        <v-tabs color="blue" dark slider-color="yellow">
                                                            <v-tab
                                                                v-for="tabs in field.groups"
                                                                :key="tabs.key"
                                                                :href="'#' + tabs.key"
                                                                ripple>
                                                                {{tabs.legend}}
                                                            </v-tab>
                                                            <v-tab-item
                                                              v-for="tabs in field.groups"
                                                              :key="tabs.key"
                                                              :id="tabs.key"
                                                            >
                                                              <v-card hover
                                                                      class="elevation-8"
                                                                      color="grey lighten-4">
                                                                <div class="ma-3">
                                                                    <div v-for="field in tabs.fields">
                                                                        <v-form-generator-field
                                                                        :field="field"
                                                                        :value="localmodel[field.model]"
                                                                        :model="localmodel"
                                                                        :fieldmodel="field.model"
                                                                        :context="context"
                                                                        :options="options"
                                                                        v-bind.sync="localmodel"
                                                                        @input="onInput"
                                                                        />
                                                                    </div>
                                                                 </div>
                                                              </v-card>
                                                            </v-tab-item>
                                                          </v-tabs>
                                                     </div>
                                                    <div v-else>
                                                            <v-form-generator-field
                                                                    :field="field"
                                                                    :value="localmodel[field.model]"
                                                                    :model="localmodel"
                                                                    :fieldmodel="field.model"
                                                                    v-bind.sync="localmodel"
                                                            />
                                                    </div>
                                                </div>
                                                </v-flex>
                                            </v-layout>
                                        </v-container>
                                   </v-card>
                                   <div class="right">
                                        <v-btn flat
                                            v-if="index+1 !== 1"
                                            @click="prevStep(index+1,schemaItem.length)">
                                                Cancel
                                        </v-btn>
                                        <v-btn color="primary"
                                            @click="nextStep(index+1,schemaItem.length)">
                                            {{ (index+1 === schemaItem.length)?"Terminar":"Continuar"}}
                                        </v-btn>
                                   </div>
                                </v-flex>
                            </v-layout>
                        </v-stepper-content>
                    </v-stepper-items>
                </v-stepper>
            </div>
            <div v-if="schemaItemIndex == 'groups'">
              <v-tabs color="blue" dark slider-color="yellow">
                <v-tab
                    v-for="group in schemaItem"
                    :key="group.key"
                    :href="'#' + group.key"
                    ripple>
                    {{group.legend}}
                </v-tab>
                <v-tab-item
                  v-for="group in schemaItem"
                  :key="group.key"
                  :id="group.key"
                >
                  <v-card flat>
                    <div class="ma-3">
                        <div v-for="field in group.fields">
                            <v-form-generator-field
                            :field="field"
                            :value="localmodel[field.model]"
                            :model="localmodel"
                            :fieldmodel="field.model"
                            v-bind.sync="localmodel"
                            @input="onInput"
                            />
                        </div>
                     </div>
                  </v-card>
                </v-tab-item>
              </v-tabs>
            </div>
            <div v-if="schemaItemIndex == 'fields'">
                <div v-for="field in schemaItem">
                    <v-form-generator-field
                    :field="field"
                    :value="localmodel[field.model]"
                    :model="localmodel"
                    :fieldmodel="field.model"
                    v-bind.sync="localmodel"/>
                </div>
            </div>
        </div>

        <v-form-generator-field-dialogBox
            ref="dialoghelp"
            v-model="dialoghelp"
            :modelSelected="localmodel"
            :dialoghelp="dialoghelp"
            :typeData="'info'"
            lazy
        />
</v-form>
</template>

<script>

import eventHub from './components/eventHub'
import ProductZoomer from 'vue-product-zoomer'

    export default{
        name: 'v-form-generator',
        props: {
            'model': Object,
            'schema': Object,
            'imageUrls': Array,
            context:Object,
            options: Object
        },
        components: {
            'v-form-generator-field': require('./form-field.vue').default,
            eventHub,
            'v-form-generator-field-dialogBox': require('./components/dialogBox.vue').default,
            ProductZoomer
        },
        computed:{
            localmodel:{
                get(){
                    return this.localmodelClone
                    //return this.cloneObject(this.model);
                },
                set (value) {
                    this.localmodelClone = JSON.parse(JSON.stringify(value));
                    //return JSON.parse(JSON.stringify(value));
                }
            },
            dialoghelp:{
                get(){
                    return this.localdialoghelp;
                },
                set(value){
                    this.localdialoghelp = value;
                }
            },
            imageArr (){
                //Objeto con un elemento raiz, y en cada uno de ellos crea una instancia de un objeto interno
                let arrimg = this.imageUrls;
                let arrFinal = []
                let i = 0;
                for( i=0;i<arrimg.length; i++){
                  arrFinal.push(
                    {
                      images: {
                        normal_size: [
                          {
                            id:i,
                            url:arrimg[i]
                          }
                        ]
                      },
                      'containerSquareOptions': {
                        'zoomFactor': 4,
                        'pane': 'container-square',
                        // 'namespace': 'inline-zoomer'+i,
                        'hoverDelay': 300,
                      }
                    }
                  )
                }
                return arrFinal;
            }
        },
        data(){
            return {
                stepper:1,
                validForm:true,
                localmodelClone:{},
                localdialoghelp:false
            }
        },
        created: function () {
            // On load

            eventHub.$on('updatefield', dataField=>{
                debugger;
                this.localmodelClone[dataField.field] = this.localmodelClone[dataField.field] = dataField.value
            })
            eventHub.$on('updatemodel', dataModel=>{
                this.updateModel(dataModel);
                this.localmodelClone = dataModel
            })
            eventHub.$on('cleanmodel', dataModel=>{
                this.localmodelClone = dataModel
            })
        },
        beforeMount(){
            if(this.model){
                this.localmodelClone = JSON.parse(JSON.stringify(this.model));
            }

        },
        methods: {
            onBlur: function(){
                console.info('blur')
            },
            onChange: function(){
                console.info('change')
            },
            onFocus: function(){
                console.info('focus')
            },
            onInput: function(value, fieldName) {
                console.log('onInput')
            },
            resetForm: function() {

                var self = this; //you need this because *this* will refer to Object.keys below`

                //Iterate through each object field, key is name of the object field`
                if(this.localmodel){
                    eventHub.$emit("updatemodel", this.localmodel)
                    this.localmodel = this.localmodelClone;
                    Object.keys(this.localmodel).forEach(function(key,index) {
                      //self.model[key] = '';
                      //self.$set(self.localmodel, key, '')
                      self.localmodel[key] = '';
                    });
                    eventHub.$emit("cleanmodel", this.localmodel)

                    //self.$emit("update:model", self.localmodel)
                    //this.model = this.localmodel
                }

            },
            nextStep (n, steps) {
                //Si es terminal se finaliza la captura
                if(this.localmodel.terminal){
                    n = steps;
                }

                if (n == steps) {
                    this.resetForm()
                    this.stepper = 1
                } else {
                  this.stepper = n + 1
                }
            },
            prevStep (n, steps) {
                this.stepper = n - 1
            },
            typeMimeType:function(item){
                let itemUpper = item.toUpperCase();
                let type="OBJECT";

                if(itemUpper.indexOf('.PDF') > -1){
                  type = "OBJECT";
                }
                else if(itemUpper.indexOf('.HTML') > -1){
                  type = "";
                }else if((itemUpper.indexOf('.GIF') > -1)
                    || (itemUpper.indexOf('.JPG') > -1)
                    || (itemUpper.indexOf('.JPEG') > -1)
                    || (itemUpper.indexOf('.PNG') > -1)
                    || (itemUpper.indexOf('.SVG') > -1) ){
                  type = "IMAGE";
                }

                return type
            },
            cloneObject(o) {
                var output, v, key;
                output = Array.isArray(o) ? [] : {};
                for (key in o) {
                    v = o[key];
                    output[key] = (typeof v === "object") ? this.cloneObject(v) : v;
                }
                return output;
            },
            updateModel:function(value){
                this.$root.$emit('updateparentmodel', value)
            },
            showHelp:function(){
                this.dialoghelp = !this.dialoghelp
            }
        }
    }
</script>
